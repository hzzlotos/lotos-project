package net.newtouch.image.implement;

/**
 * 
 * 图片类型：
 * MATERIAL表示物资图片
 * WAREHOUSE表示仓库图片
 * PORTRAIT表示头像图片
 * SESSION表示专场图片
 * CERTIFICATE表示证件图片
 * ID_CARD表示身份证图片
 * LOGISTICS表示物流图片
 * LOGO表示物流商Logo图片
 * 
 * @author hezhengzheng
 * @version 1.0
 * @since 2014年6月20日
 */
public enum ImageType {
    MATERIAL, // 物资
    WAREHOUSE, // 仓库
    PORTRAIT, // 头像
    SESSION, // 专场
    CERTIFICATE, // 证件
    ID_CARD, // 身份证
    LOGISTICS, // 物流
    LOGO // 物流商Logo
}
