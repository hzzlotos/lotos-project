package cn.newtouch.base.base;

public class IntegerGet16
{
    public static void main(String[] args)
    {
        System.out.println(Integer.toString(18, 12));// 按12进制计算18
        System.out.println(Integer.toString(18, 2));// 按2进制计算18
        System.out.println(Integer.toString(18, 16));// 按2进制计算18
        System.out.println(Integer.toString(10, 16));
        System.out.println(Integer.toString(11, 16));
        System.out.println(Integer.toString(15, 16));
        System.out.println(Integer.toString(16, 16));
    }
}