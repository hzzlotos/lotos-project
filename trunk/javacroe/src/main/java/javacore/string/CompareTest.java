package javacore.string;

public class CompareTest
{
    public static void main(String[] args)
    {
        String str = "a";
        String str1 = "u";
        // 大于0表示str大于str1
        // 小于0表示str小于str1
        // 等于0表示str=str1
        System.out.println(str.compareTo(str1));
    }
}
